public class Carro extends Veiculo{
    double potenciaMotorCarro;
    int numeroPortasCarro;

    public Double getPotenciaMotorCarro() {
        return this.potenciaMotorCarro;
    }

    public void setPotenciaMotorCarro(Double potenciaMotorCarro) {
        this.potenciaMotorCarro = potenciaMotorCarro;
    }

    public int getNumeroPortasCarro() {
        return this.numeroPortasCarro;
    }

    public void setNumeroPortasCarro(int numeroPortasCarro) {
        this.numeroPortasCarro = numeroPortasCarro;
    }
}
